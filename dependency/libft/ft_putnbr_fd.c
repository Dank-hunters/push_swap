#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	i;

	i = 0;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	else
	{
		if (n < 0)
		{
			n = n * -1;
			write(fd, "-", 1);
		}
		i = n % 10 + 48;
		if (n >= 10)
			ft_putnbr_fd((n / 10), fd);
		write(fd, &i, 1);
	}
	return ;
}
