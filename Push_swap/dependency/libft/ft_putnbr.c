#include "libft.h"

void	ft_putnbr(int n)
{
	int	i;

	i = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	else
	{
		if (n < 0)
		{
			n = n * -1;
			write(1, "-", 1);
		}
		i = n % 10 + 48;
		if (n >= 10)
			ft_putnbr(n / 10);
		write(1, &i, 1);
	}
	return ;
}
